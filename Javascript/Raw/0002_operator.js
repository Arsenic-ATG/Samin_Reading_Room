let x = 5;
let y = 5;
// equal value & type operator "===" , this will result true only when the variable type
// value & type both are same
(x === y ) ? console.log("True") : console.log("False");

// normal comparison operator "==" only compare value not type

// exponential
// first value consider as value, second one as power.
let exp = x ** y;
console.log(exp);
