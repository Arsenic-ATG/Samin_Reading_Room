a = 10
b = 2.7

# add a with b
print(a + b)

# subtract a with b
print(a - b)

# multiply a with b
print(a * b)

# divide a with b
print(a / b)

# raise a to the power of b. If b is an no-integer, it will become a root. 
print(a ** b)

# gives the integral part of a divided by b.
print(a // b)

# gives the remainder of a divided by b.
print(a % b)
